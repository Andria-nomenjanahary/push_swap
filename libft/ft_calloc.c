#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	unsigned char	*ptr;
	size_t			i;
	size_t			total;

	total = nmemb * size;
	if (nmemb != 0 && (size > total / nmemb))
		return (NULL);
	ptr = malloc((nmemb * size) * sizeof(char));
	if (!ptr)
		return (NULL);
	i = 0;
	while (i < nmemb * size)
	{
		ptr[i] = 0;
		i++;
	}
	return (ptr);
}
